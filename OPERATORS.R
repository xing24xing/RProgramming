a<-7
b<-2
print(a+b)#addition
print(a-b)#subtraction
print(a*b)#Multiplication
print(a/b)#division
print(a%%b) #remainder
print(a%/%b)#Quotient
print(a^b)#power

#Vectors
c1<-c(4,5,6,7)
c2<-c(2,5,8,9)
print(c1+c2)#addition
print(c1-c2)#subtraction
print(c1*c2)#Multiplication
print(c1/c2)#division
print(c1%%c2) #remainder
print(c1%/%c2)#Quotient
print(c1^c2)#power

#Relational Operator
a<-7
b<-2
print(a>b)#greater
print(a<b)#lesser
print(a==b)#Equal
print(a<=b)#less equal
print(a>=b) #greater equal
print(a!=b)#not equal


